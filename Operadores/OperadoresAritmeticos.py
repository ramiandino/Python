# (suma y resta) ( + y -)
suma = 12 + 5
resta = 10 - 5

# (multiplicacion y division) (* y /)
multi = 12 * 5
division = 12 / 5  #nos devuelve un dato float (flotante)

#Potenciacion (exponente)  (**)

exponente = 12 ** 5

#division baja (//)

divisionBaja = 12 // 5   #devuelve entero redodeando hacia abajo

#resto o modulo 

resto = 12 % 5

tipoDeDato = type(divisionBaja)  #type nos devuelve que tipo de dato es.   

print(suma)
print(resta)
print(multi)
print(division)
print(exponente)
print(divisionBaja)
print(resto)
print(tipoDeDato)